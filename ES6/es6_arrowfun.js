//function expression

var myfun1=function show(){
    console.log("It is a function expression");
}

//Anonymous function

var myfun2=function(){
    console.log("It is anonymous function")
}

//arrow function

var myfun3=()=>{
    console.log("It is an arrow function")
};

myfun1();
myfun2();
myfun3();
